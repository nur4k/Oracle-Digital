from django.conf import settings
from django.conf.urls.static import static
from django.contrib import admin
from django.urls import include, path
from drf_yasg import openapi
from drf_yasg.views import get_schema_view
from rest_framework import permissions
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
    TokenVerifyView,
)

schema_view = get_schema_view(
    openapi.Info(
        title="Oracle Digital API",
        default_version="v1",
        description="Oracle Digital",
        terms_of_service="",
        contact=openapi.Contact(email="nurs@gmail.com"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path("swagger/", schema_view.with_ui(), name="schema-json"),
    path("admin/", admin.site.urls),
    # path('api/rest_framework/', include('rest_framework.urls')),
    # path('auth/', include('rest_framework.urls')),
    path("swagger/", schema_view.with_ui(), name="schema-json"),
    # path('api/', include(urlpatterns_apps)),
    # JWT
    path("api/login/", TokenObtainPairView.as_view(), name="token_obtain_pair"),
    path("api/login/refresh/", TokenRefreshView.as_view(), name="token_refresh"),
    path("api/login/verify/", TokenVerifyView.as_view(), name="token_verify"),
    path("api/teacher/", include("apps.teacher.urls")),
    path("api/student/", include("apps.student.urls")),
]

urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
